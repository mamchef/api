<h1>MAMCHEF BENDROSIOS SĄLYGOS<br>PARTNERIAMS</h1>

<p><strong>Įsigaliojimo data:</strong> 2025-09-05</p>

<p>Šios bendrosios sąlygos (toliau – „Sąlygos“) reglamentuoja santykius tarp MamChef, kurią valdo MB „MamChef“, įsteigtos pagal Lietuvos Respublikos teisę, įmonės kodas 307277177, registruotos adresu Krivių g. 5, LT-01204, Vilnius, Lietuvos Respublika (toliau – „Platforma“), ir fizinio ar juridinio asmens, naudojančio Platformą maistui parduoti (toliau – „Partneris“). Registruodamasis ar naudodamasis Platforma Partneris patvirtina, kad sutinka būti saistomas šiomis Sąlygomis.</p>

<h2>PLATFORMOS VAIDMUO</h2>

<p>Platforma suteikia Partneriams galimybę gauti maisto užsakymus iš Vartotojų. Šie užsakymai, priklausomai nuo Vartotojo pasirinkto pristatymo būdo, gali būti perduodami Vartotojui atsiėmimui arba pristatomi trečiosios šalies pristatymo teikėjo.</p>

<p>Visiems sandoriams per Platformą taikoma, kad maisto įsigijimas yra tiesioginė sutartis (toliau – „Pardavimo sutartis“) tarp Vartotojo ir Partnerio. Jei numatytas pristatymas, atskira sutartis (toliau – „Pristatymo sutartis“) sudaroma tiesiogiai tarp Vartotojo ir pristatymo paslaugą teikiančio asmens ar subjekto, kuriuo gali būti Partneris arba trečioji šalis.</p>

<p>Pardavimo sutartis laikoma sudaryta nuo momento, kai Partneris patvirtina Vartotojo užsakymą ir, kai taikoma, kai pristatymo teikėjas patvirtina dalyvavimą ir Vartotojui išsiunčiamas užsakymo patvirtinimas. Atsiėmimo (self pick-up) atveju Pardavimo sutartis laikoma sudaryta, kai Partneris priima užsakymą ir išsiunčiamas patvirtinimas.</p>

<p>Jeigu užsakymas pateikiamas tiesiogiai vietoje, Pardavimo sutartis įsigalioja, kai Vartotojas pateikia užsakymą Partneriui ir Partneris jį priima.</p>

<p>Visos tokios sutartys sudaromos pagal standartinį pasiūlymo ir priėmimo principą, laikantis taikytinos Lietuvos sutarčių teisės. Partneris prisiima visą teisinę atsakomybę už maisto paruošimą, saugą ir kokybę, o pristatymo šalis – už logistiką ir pristatymą.</p>

<p>Partneris paskiria Platformą savo įgaliotu komerciniu atstovu, siekiant palengvinti sandorius tarp Partnerio ir Vartotojų, įskaitant, kai taikoma, pristatymo susitarimus. Partneris leidžia Platformai veikti jo vardu sudarant Pardavimo (ir, kai taikoma, Pristatymo) sutartis su Vartotojais, įskaitant, bet neapsiribojant, atsiskaitymų už maistą, pristatymo mokesčius ir kitus Partneriui mokėtinus mokesčius.</p>

<p>Platforma įgaliota surinkti tokius mokėjimus ir pervesti juos Partneriui pagal šias Sąlygas. Partneris pripažįsta, kad Vartotojo piniginė prievolė laikoma įvykdyta, kai mokėjimą sėkmingai apdoroja Platforma.</p>

<p>Jei Partneris pasirenka pats organizuoti pristatymą, Vartotojas sudaro tiesioginę Pristatymo sutartį su Partneriu. Tokiu atveju visos šiose Sąlygose trečiųjų šalių pristatymo teikėjams taikomos nuostatos mutatis mutandis taikomos Partneriui kaip Pristatymo sutarties šaliai.</p>

<p>Partneris vienas atsako už visišką teisinį atitiktinumą teikiant pristatymo paslaugas, įskaitant, kai taikoma, darbuotojų ar subrangovų privalomus mokymus, sertifikavimus ir atnaujintus leidimus, licencijas bei kitus pagal Lietuvos teisę reikalingus leidimus.</p>

<p>Be to, Partneris privalo užtikrinti, kad naudojami termo krepšiai būtų švarūs, prižiūrėti ir tinkami maisto pristatymui taip, kad būtų išlaikoma higiena, išvengta kryžminės taršos ir maistas atvyktų saugus vartoti.</p>

<p>Partneris gali pasirinkti vieną ar kelis Platformoje siūlomus paslaugų modelius: pristatymą, atsiėmimą (self pick-up) ar kitus Platformos siūlomus sprendimus. Partneris gali įjungti šias parinktis atskirai ar kartu, priklausomai nuo prieinamumo ir techninių galimybių.</p>

<p>Konkretaus modelio sąlygos taikomos tik tuo atveju, jei Partneris pasirinko tuo modeliu naudotis. Į kito modelio nuostatas Partneris nėra įtraukiamas.</p>

<p>Platforma suteikia Partneriui pilną prieigą ir kontrolę prie savo paskyros: valdyti prekių sąrašus, kainas, prieinamumą ir verslo informaciją. Kai skelbiami teisiškai reguliuojami produktai (pvz., alkoholiniai gėrimai ar kitos ribojamos prekės), Partnerio paskyra laikoma Partnerio internetine parduotuve.</p>

<p>Partneris vienas atsako už tai, kad visi skelbimai ir pardavimai atitiktų taikytinus teisės aktus – licencijas, amžiaus apribojimus, ženklinimą, aprašymus ir reklamos taisykles – ir kad būtų palaikomi galiojantys leidimai.</p>

<p>Partneris įgalioja Platformą ir/ar paskirtus pristatymo teikėjus atlikti tam tikras teisines patikras Partnerio vardu, įskaitant minimalios amžiaus ribos tikrinimą ir prekybos laiko ribojimų laikymąsi. Tačiau Partneris lieka visiškai atsakingas už bet kokius teisės pažeidimus ir įsipareigoja atlyginti Platformai žalą dėl baudų, sankcijų, pretenzijų ar skundų, kylančių iš reguliuojamų produktų prekybos ar reklamos.</p>

<p>Partneris privalo pateikti Platformai visą informaciją ir dokumentus, reikalingus pagal taikomus teisinius režimus, įskaitant, bet neapsiribojant, ES Reglamentą 2022/2065 (Skaitmeninių paslaugų aktas) ir Tarybos Direktyvą 2021/514 (DAC7). Pateikti duomenys privalo būti tikslūs, išsamūs ir nuolat atnaujinami.</p>

<p>Pasikeitus Partnerio duomenims (pavadinimui, adresui, kontaktams, mokėjimo ar mokesčių informacijai, įskaitant PVM ar TIN), Partneris nedelsdamas informuoja Platformą ir suteikia pakankamai laiko įrašams atnaujinti bei įvykdyti reguliacinius įsipareigojimus.</p>

<p>Nepateikus tikslios ar aktualios informacijos gali vėluoti išmokos, būti sustabdytos paslaugos ar kilti kitų trikdžių. Platforma, kiek leidžia įstatymai, neatsako už nuostolius, kylančius dėl Partnerio pareigų nevykdymo.</p>

<h2>PARTNERIO STATUSAS IR PAREIGOS</h2>

<p>Partneris veikia kaip nepriklausomas maisto verslo operatorius, turintis Valstybinės maisto ir veterinarijos tarnybos (VMVT) leidimą.</p>

<p>Partneris patvirtina, kad laikosi visų taikytinų maisto teisės reikalavimų, įskaitant higieną, alergenų ženklinimą ir sanitarinius standartus, ir prisiima visą atsakomybę už saugą, tikslumą ir teisėtumą.</p>

<p>Partneris atlygina Platformai visus reikalavimus, žalą ar bylas, kylančias dėl apsinuodijimų, alergenų, užterštumo ar kitų su maistu susijusių problemų.</p>

<p>Registracija galima tik pateikus: galiojantį VMVT leidimą, tapatybės/įmonės tikrinimą, banko sąskaitos patvirtinimą ir kitus Platformos prašomus dokumentus. Platforma gali atsisakyti priimti Partnerį savo nuožiūra.</p>

<p>Partneris privalo pranešti Platformai apie teisinio, reguliacinio ar veiklos statuso pokyčius. Nesilaikymas gali lemti sustabdymą ar nutraukimą.</p>

<h2>UŽSAKYMAI IR PARDAVIMAI</h2>

<p>Vartotojui pateikus Užsakymą, Partneris privalo per Platformos nurodytą reakcijos laiką peržiūrėti ir priimti arba atmesti Užsakymą.</p>

<p>Negalėdamas įvykdyti dėl ingredientų trūkumo, pajėgumų ar kitų priežasčių, Partneris neturi priimti Užsakymo. Net ir esant automatinio priėmimo funkcijai, Partneris privalo laikytis paslaugų lygių.</p>

<p>Partneris privalo aktyviai stebėti gaunamus Užsakymus savo skelbtu darbo laiku. Vėlavimas gali lemti suspendavimą ar reitingo mažinimą.</p>

<h2>UŽSAKYMŲ VYKDYMAS</h2>

<p>Partneris privalo būti pasiruošęs pradėti teikti paslaugas iškart aktyvavus paskyrą ar sudarius Sutartį, jei raštu nesutarta kitaip.</p>

<p>Konkrečios procedūros ir terminai pateikiami Paslaugų lygių gairėse (1 priedas) ir yra šių Sąlygų dalis.</p>

<p>Partneris turi dėti pagrįstas pastangas, kad meniu prekės būtų prieinamos darbo valandomis. Negalėdamas vykdyti, turi sustabdyti užsakymų priėmimą (offline).</p>

<p>Partneris atsako, kad kiekvienas Užsakymas atitiktų Vartotojo pasirinktus elementus ir pastabas. Specialūs prašymai tenkinami, jei saugu ir pagrįsta.</p>

<p>Visi Užsakymai turi būti paruošti per Paslaugų lygių politikoje nustatytus terminus. Partneris gali įjungti/išjungti atsiėmimą savo nuožiūra, jei nesutarta kitaip.</p>

<p>Partneris privalo patikrinti, kad teisingas Užsakymas atiduodamas teisingam kurjeriui ar Vartotojui. Netinkamai atidavus, Platforma gali išskaičiuoti Užsakymo vertę. Vėlavimo atveju gali būti taikomos proporcingos išskaitos iki užsakymo kainos.</p>

<p>Visos prekės turi būti tinkamai supakuotos, laikantis teisės aktų; tik sandariai uždarytos ir suženklintos išduodamos atsiėmimui. Pristatymo atveju maistas turi būti tinkamas transportavimui termo dėžėje. Pakuotė neturi turėti konkurentų ar trečiųjų šalių reklamos be Platformos leidimo.</p>

<p>Partneris garantuoja, kad pardavimo metu prekės atitinka šiuos kriterijus:</p>

<p><strong>Tikslus aprašymas:</strong> Prekės turi atitikti Platformoje skelbiamus aprašymus, įskaitant dietinius žymenis („be gliuteno“, „be riešutų“, „vegetariška“, „veganams“) – teiginiai turi būti teisingi ir patikrinami.</p>

<p><strong>Sveikata ir sauga:</strong> Prekės turi būti saugios vartoti ir nekenksmingos, be draudžiamų ar pavojingų medžiagų.</p>

<p><strong>Teisinis atitikimas:</strong> Prekės negali pažeisti teisės aktų ar šių Sąlygų.</p>

<p><strong>Tinkamas paruošimas:</strong> Maistas turi būti tinkamai pagamintas ir pateiktas, užtikrinant higieną, saugą ir tinkamą temperatūrą transportavimo metu.</p>

<p><strong>Pakuotės standartai:</strong> Pakuotė turi atitikti teisės aktus ir gerą praktiką, būti pakankamai patvari.</p>

<p><strong>Vizualinis tikslumas:</strong> Išvaizda, kiek įmanoma, turi atitikti Platformoje pateiktas nuotraukas.</p>

<p>Partneris privalo nedelsdamas informuoti Platformą, sužinojęs, kad bet kuris jo skelbiamas produktas gali būti neteisėtas, klaidingai pateiktas ar neatitinkantis reikalavimų.</p>

<p>Platforma turi teisę bet kada be įspėjimo atlikti Partnerio veiklos patikras ar auditą. Partneris įsipareigoja bendradarbiauti ir teikti informaciją, prieigą bei teisingus atsakymus.</p>

<p>Platforma gali vienašališkai atšaukti Užsakymą šiais atvejais:</p>

<p><strong>Vartotojo atšaukimas iki priėmimo:</strong> Jei Vartotojas atšaukia prieš Partneriui patvirtinant – Partneriui kompensacija nepriklauso.</p>

<p><strong>Atšaukimas iki gamybos pradžios:</strong> Jei atšaukta iki gamybos pradžios ir nebuvo žymos apie gamybą – mokėjimas nepriklauso.</p>

<p><strong>Vartotojas atšaukė po priėmimo, bet prieš gamybą:</strong> Mokestis nemokamas.</p>

<p><strong>Atšaukimas gamybos metu, dar nepasibaigus pristatymo laikui:</strong> Kompensuojama tik už gamybos reikalavusias prekes; paruoštoms iš anksto (pvz., gėrimai buteliuose) – ne.</p>

<p><strong>Atšaukimas po numatyto pristatymo laiko:</strong> Jei vėlavimas dėl Partnerio – mokestis nepriklauso, pristatymo išlaidos gali būti išskaičiuotos.</p>

<p><strong>Atšaukimas prieš atsiėmimą:</strong> Galima kompensacija pagal 7 skyrių, išskyrus iš anksto supakuotas prekes.</p>

<p>Tinkamumą kompensacijai Platforma nustato pagal sistemos įrašus ir gamybos būseną; sprendimas galutinis.</p>

<p>Jei Partneris pristato pats, jis garantuoja pilną atitiktį visiems teisės aktams ir standartams; tai taikoma ir darbuotojams bei subrangovams.</p>

<p>Platforma gali įgalinti lankstų kainodaros modelį (pagal svorį, pakaitalai, papildomas pakuotės mokestis).</p>

<p>Tokiu atveju pradinės kainos laikomos įkainiais. Platforma gali atlikti laikinas rezervacijas arba taikyti preliminarų koregavimą.</p>

<p>Prieš išsiunčiant ar atiduodant Užsakymą, Partneris privalo pateikti galutinę kainą (svoris, pakuotė, pakaitalai). Ji turi sutapti su fiskaliniu kvitu ar sąskaita.</p>

<p>Vartotojai apmokestinami tik už faktiškai gautas prekes. Jei preliminari suma per maža, skirtumą dengia Partneris. Už netikslumus Platforma gali išskaičiuoti permokas iš išmokų.</p>

<p>Platforma gali leisti Partnerio lojalumo naudų taikymą atsiskaitymo metu; Partneris vienas atsako už konfigūravimą, valdymą ir tikrinimą. Galutinė kaina turi atitikti kvitą/sąskaitą.</p>

