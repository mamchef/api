<p>Vartotojai gali teikti atsiliepimus ir skundus per Platformą. Jei skundas dėl konkrečių prekių, jis teikiamas: per 48 val. – gendančioms; per 30 d. – negendančioms; bet kada – dėl galimos maisto saugos rizikos.</p>

