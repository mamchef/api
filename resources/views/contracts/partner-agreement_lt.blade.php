<p>Platforma gali sustabdyti prieigą, jei Partneris kartotinai teikia akivaizdžiai neteisėtą informaciją, apie tai iš anksto įspėjus ir suteikus galimybę pasitaisyti; sprendimas proporcingas.</p>

