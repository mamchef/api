<p>Prekėms, kurioms taikomas Bendrosios gaminių saugos reglamentas (ES) 2023/988, Partneris vienas atsako už saugą, ženklinimą ir informaciją Platformoje.</p>

<p>Nustačius neteisėtą ar Sąlygas pažeidžiantį turinį, Platforma gali: pašalinti turinį; apriboti įkėlimą; laikinai ar visam laikui sustabdyti prieigą; pranešti institucijoms; sulaikyti išmokas; inicijuoti tyrimą; nutraukti sutartį. Partneris turi teisę teikti paaiškinimus per vidinę tvarką.</p>

