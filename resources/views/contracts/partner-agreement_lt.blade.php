<p>Partneris valdo prieigos teises pavaldiniams ir atsako už jų veiksmus: gali redaguoti meniu ir kainas, kampanijas, peržiūrėti užsakymus, valdyti paskyras ir nustatymus.</p>

<h2>MOKĖJIMAI IR MOKESČIAI</h2>

<p>Platforma renka mokėjimus Vartotojų vardu Partneriui ir išskaito taikomus mokesčius pagal šias Sąlygas ir priedus.</p>

<p>Už tarpininkavimą Partneris moka Platformos mokestį už kiekvieną Platformoje pateiktą Užsakymą, išskyrus savanoriškus arbatpinigius.</p>

<p>Mokestis skaičiuojamas kaip sutartas procentas nuo Partnerio grynųjų pajamų (be PVM) už atitinkamą atsiskaitymo laikotarpį. Konkreti procentinė dalis ir sąlygos nurodomos komercinėse sąlygose, onboarding’o dokumentuose ar dinaminiame Priede. Platforma gali pakeisti procentą, apie tai pranešusi prieš 30 dienų; nesutikdamas Partneris gali nutraukti.</p>

